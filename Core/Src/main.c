/**
 * @brief Imposta l'angolo del servomotore (0–90°)
 * @param angle Angolo desiderato in gradi
 */
void Set_Servo_Angle(uint8_t angle)
{
    if (angle > 90) angle = 90;

    // Range tipico e sicuro per SG90/MG90S
    const uint16_t SERVO_MIN_US = 10;  // impulso per 0°
    const uint16_t SERVO_MAX_US = 20;  // impulso per 90°

    uint32_t pulse_length = SERVO_MIN_US +
                            ((uint32_t)angle * (SERVO_MAX_US - SERVO_MIN_US) / 90U);

    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, pulse_length);
}
